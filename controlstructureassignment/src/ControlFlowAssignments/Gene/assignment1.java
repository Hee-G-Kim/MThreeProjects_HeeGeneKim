package ControlFlowAssignments.Gene;

public class assignment1 {

	public static void main(String[] args) {
		/*Assignment 1
		a. Create a class SwitchCaseTest with main() method
		b. Declare and initialize a character variable with any 
		alphabet c. Find if the character is a vowel and print thE result*/
		
		char c = 'e';
			
		switch (c) {
		case 'a':
			System.out.println(c + " is a vowel");
			break;
		case 'e':
			System.out.println(c + " is a vowel");
			break;
		case 'i':
			System.out.println(c + " is a vowel");
			break;
		case 'o':
			System.out.println(c + " is a vowel");
			break;
		case 'u':
			System.out.println(c + " is a vowel");
			break;
		default:
			break;
		}
	}

}
